#include <stdio.h>

int main(int argc, char *argv[])
{ 
    int a[5] = {1,2,3,4,5}, *p = &a[0];

    for(int i = 0; i < 2; i++)
    {
        *(p+i) ^= *(p+4-i);
        *(p+4-i) ^= *(p+i);
        *(p+i) ^= *(p+4-i);
    }

    for(int i = 0; i < 5; i++)
        printf("%d ", *p++);
    puts("");

    return 0;
} 
