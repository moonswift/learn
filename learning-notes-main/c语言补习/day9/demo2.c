#include <stdio.h>

int *fun()
{
    //int a[3] = {1,2,3};
    static int a[3] = {1,2,3};
    //a是局部变量的地址,当函数调用结束,该地址对应空间被系统自动释放
    return a; //err
}

int main(int argc, char *argv[])
{ 
    //接收的是局部变量的地址
    int *p = fun();
    printf("%d\n", *p);
    return 0;
} 
